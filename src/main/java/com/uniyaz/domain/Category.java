package com.uniyaz.domain;


public class Category extends BaseDomain {
    private int id;
    private String name;
    @Override
    public int getId() {
        return id;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
